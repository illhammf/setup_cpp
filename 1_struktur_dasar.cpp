#include <iostream>
using namespace std;

// Fungsi utama
int main(){
    int num1 = 5; // Deklarasi dan Inisialisasi variabel secara langsung
    int num2 = 10;

    int sum; // Deklarasi variabel
    sum = num1 + num2; // Melakukan penjumlahan

    // Menampilkan hasil ke layar
    cout << "Hasil pemjumlahan dari " << num1 << " + " << num2 << " adalah " << sum << endl;
    
    return 0; // Mengakhiri program
}