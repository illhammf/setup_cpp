#include <iostream>
#include <string>
using namespace std;

int main() {
    
    // Deklarasi dan inisialisasi variabel
    // Tipe data integer
    int umur = 19;

    // Tipe data float
    float berat = 51.05;

    // Tipe data double
    double phi = 3.14159;

    // Tipe data char
    char huruf = 'A';

    // Tipe data bool
    bool nilaiBenar = true;

    // Tipe data string
    string nama = "Ilham";

    // Menampilkan contoh tipe data di atas
    cout << umur << endl;
    cout << berat << endl;
    cout << phi << endl;
    cout << huruf << endl;
    cout << nilaiBenar << endl;
    cout << nama << endl;

    return 0;
}